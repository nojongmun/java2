class Ex03{
      public static void main(String[] args){
          // 데이터 타입 : 처리할 수 있는 자료의 형태
          //             정수, 실수, 문자, 논리, 문자열 또는 클래스

          System.out.println(245);     // 정수
          System.out.println(3.14);    // 실수
          // 주석 : 컴파일러가 컴파일을 하지 않는 줄을 말한다.
          //     : 사람들에게 해당 줄의 의미를 쉽게 풀어 높은 줄
          // System.out.println('c');  // 문자
          System.out.println("a");     // 문자열, 클래스
          System.out.println(true);    // 논리
      }
}