package com.ict.edu;

public class Ex02 {
	// 멤버필드 : 상태값, 속성
	// - static X = instance  ,
	// - static O = static,
	// - final X = 변수
	// - final O = 상수
	String name = "홍길동";        // instance 변수
	int kor = 80 ;               // instance 변수 
	static int eng = 90 ;        // static 변수
	final int MATH = 100 ;       // instance 상수
	static final int COM = 100 ; // static 상수
}
